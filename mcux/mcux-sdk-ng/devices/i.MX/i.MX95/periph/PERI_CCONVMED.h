/*! @name CTRL_CAM - Camera 0 CCONVMED Control Register */
/*! @{ */

#define CCONVMED_CTRL_CAM_FLT_MASK               (0x30U)
#define CCONVMED_CTRL_CAM_FLT_SHIFT              (4U)
/*! FLT
 *  0b00..Bypassed
 *  0b01..Filter type is convolution (5x5 binomial)
 *  0b10..Filter type is median (5x5)
 *  0b11..Reserved
 */
#define CCONVMED_CTRL_CAM_FLT(x)                 (((uint32_t)(((uint32_t)(x)) << CCONVMED_CTRL_CAM_FLT_SHIFT)) & CCONVMED_CTRL_CAM_FLT_MASK)
/*! @} */

