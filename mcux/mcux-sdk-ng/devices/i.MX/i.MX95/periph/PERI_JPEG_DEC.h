/*! @name CTRL - Control */
/*! @{ */

#define JPEG_DEC_CTRL_LP_MASK                    (0x1U)
#define JPEG_DEC_CTRL_LP_SHIFT                   (0U)
/*! LP - Low Power
 *  0b0..No effect
 *  0b1..Enable
 */
