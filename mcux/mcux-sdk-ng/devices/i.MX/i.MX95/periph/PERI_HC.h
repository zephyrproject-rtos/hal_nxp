/*! @name CTRL_CAM - Camera 0 Head Color Control */
/*! @{ */

#define HC_CTRL_CAM_HOFFSET_MASK                 (0x3U)
#define HC_CTRL_CAM_HOFFSET_SHIFT                (0U)
/*! HOFFSET
 *  0b00..RED component is at 0 th pixel position.
 *  0b01..RED component is at 1 st pixel position.
 *  0b10..RED component is at 2 nd pixel position.
 *  0b11..RED component is at 3 rd pixel position.
 */
#define HC_CTRL_CAM_HOFFSET(x)                   (((uint32_t)(((uint32_t)(x)) << HC_CTRL_CAM_HOFFSET_SHIFT)) & HC_CTRL_CAM_HOFFSET_MASK)

#define HC_CTRL_CAM_VOFFSET_MASK                 (0xCU)
#define HC_CTRL_CAM_VOFFSET_SHIFT                (2U)
/*! VOFFSET
 *  0b00..RED component line is at 0 th image line.
 *  0b01..RED component line is at 1 st image line.
 *  0b10..RED component line is at 2 nd image line.
 *  0b11..RED component line is at 3 rd image line.
 */
#define HC_CTRL_CAM_VOFFSET(x)                   (((uint32_t)(((uint32_t)(x)) << HC_CTRL_CAM_VOFFSET_SHIFT)) & HC_CTRL_CAM_VOFFSET_MASK)
/*! @} */

/* The count of HC_CTRL_CAM */
#define HC_CTRL_CAM_COUNT                        (1U)


