#define NEUTRON_FORMAT_SCALER_SIZE(x)            (((uint32_t)(((uint32_t)(x)) << NEUTRON_FORMAT_SCALER_SIZE_SHIFT)) & NEUTRON_FORMAT_SCALER_SIZE_MASK)

