    *(volatile uint32_t *)((uintptr_t)base + CORE_DIG_IOCTRL_RW_AFE_LANE2_CTRL_2_3_OFFSET) &= ~0x100U;
    *(volatile uint32_t *)((uintptr_t)base + CORE_DIG_IOCTRL_RW_AFE_CB_CTRL_2_6) |= 0x1000U;
    base->PPI_RW_COMMON_CFG = 0x3U;
