    base->PPI_STARTUP_RW_COMMON_DPHY_6 = 0x10U;
    base->PPI_STARTUP_RW_COMMON_DPHY_A = 0x21U;
    *(volatile uint32_t *)((uintptr_t)base + CORE_DIG_ANACTRL_RW_COMMON_ANACTRL_2_OFFSET) = 0x1444U;
    base->PPI_CALIBCTRL_RW_COMMON_BG_0 = 0x1F4U;
    base->PPI_RW_TERMCAL_CFG_0 = 0x17U;
    base->PPI_RW_LPDCOCAL_TIMEBASE = 0x5FU;
    base->PPI_RW_LPDCOCAL_NREF = 0x320U;
    base->PPI_RW_LPDCOCAL_NREF_RANGE = 0xFU;
    base->PPI_RW_LPDCOCAL_TWAIT_CONFIG = 0xFE1DU;
    base->PPI_RW_LPDCOCAL_VT_CONFIG = 0xEBCU;
    base->PPI_RW_LPDCOCAL_COARSE_CFG = 0x105U;
    *(volatile uint32_t *)((uintptr_t)base + CORE_DIG_IOCTRL_RW_AFE_CB_CTRL_2_2) = 0x8000U;
    base->PPI_RW_PLL_STARTUP_CFG_0 = 0xEFU;
    base->PPI_RW_PLL_STARTUP_CFG_1 = 0x77U;
    base->PPI_RW_PLL_STARTUP_CFG_2 = 0U;
    *(volatile uint32_t *)((uintptr_t)base + CORE_DIG_IOCTRL_RW_AFE_LANE0_CTRL_2_3_OFFSET) &= ~0x100U;
    *(volatile uint32_t *)((uintptr_t)base + CORE_DIG_IOCTRL_RW_AFE_LANE1_CTRL_2_3_OFFSET) &= ~0x100U;
    *(volatile uint32_t *)((uintptr_t)base + CORE_DIG_IOCTRL_RW_AFE_LANE3_CTRL_2_3_OFFSET) &= ~0x100U;
