#ifndef _TYPE_DEF_H
#define _TYPE_DEF_H

#include <stdint.h>

/** Unsigned char */
#define u8 uint8_t
/** Unsigned short */
#define u16 uint16_t
/** Unsigned long integer */
#define u32 uint32_t
/** Unsigned long long integer */
#define u64 uint64_t
/** Signed character, 1 byte */
#define s8 int8_t
/** Signed character, 2 byte */
#define s16 int16_t
/** Signed character, 4 byte */
#define s32 int32_t
/** Signed character, 8 byte */
#define s64 int64_t

/** Character pointer */
#define PCHAR char *
/** Unsigned character pointer */
#define PUCHAR unsigned char *
/** Unsigned character */
#define UCHAR unsigned char
/** Unsigned integer */
#define UINT unsigned int

#endif /* _TYPE_DEF_H */
